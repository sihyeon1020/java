package 반복문;


public class For3 {

	public static void main(String[] args) {
		// 1.
		for (int i = 0; i < 10; i++) {
			System.out.print("*");
		}
		System.out.println();
		
		// 2.
		for (int i1 = 0; i1 < 5; i1++) {
			System.out.print("커피*");
		}
		System.out.println();
		
		//3.
		for (int i2 = 0; i2 < 3; i2++) {
			System.out.println("커피*우유");
		}
		
		//4.
		for (int i3 = 0; i3 < 3; i3++) {
			System.out.println(i3 + 1  + " : 짱!");
		}
	}
}
